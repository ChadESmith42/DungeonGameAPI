using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;

namespace DungeonGameAPI.Utilities
{
    public class NameGenerator
    {
        public static String MakeCharacterName(String name, String descriptor)
        {
            string characterName = "${0} the {1}", name, descriptor;

            return characterName;
        }
    }
}
